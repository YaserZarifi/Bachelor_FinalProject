# حداقل امتیاز برای اینکه به یک دسته تعلق بگیرد
MIN_CATEGORY_SCORE = 2
# حداقل اطمینان نسبی (امتیاز برنده / کل امتیاز) 
CONFIDENCE_THRESHOLD = 0.35


def classify_text(text: str) -> dict:
    """
    متن گزارش را دسته‌بندی می‌کند.

    Returns:
        {
            "category": str | None,     # نام دسته پیشنهادی
            "confidence": float,         # میزان اطمینان (0.0 - 1.0)
            "scores": dict,              # امتیاز همه دسته‌ها
            "needs_ai_fallback": bool,   # آیا نیاز به Claude API دارد؟
        }
    """
    scores: dict[str, int] = {}

    for category, keywords in CATEGORY_KEYWORDS.items():
        score = 0
        for keyword, weight in keywords:
            if keyword in text:
                score += weight
        if score > 0:
            scores[category] = score

    if not scores:
        return {
            "category": None,
            "confidence": 0.0,
            "scores": {},
            "needs_ai_fallback": True,
        }

    # پیدا کردن دسته با بیشترین امتیاز
    best_category = max(scores, key=lambda k: scores[k])
    best_score = scores[best_category]
    total_score = sum(scores.values())
    confidence = best_score / total_score if total_score > 0 else 0.0

    needs_fallback = (
        best_score < MIN_CATEGORY_SCORE
        or confidence < CONFIDENCE_THRESHOLD
    )

    logger.debug(
        f"[Categorizer] best='{best_category}' score={best_score} "
        f"confidence={confidence:.2f} fallback={needs_fallback}"
    )

    return {
        "category": best_category if not needs_fallback else None,
        "confidence": round(confidence, 3),
        "scores": scores,
        "needs_ai_fallback": needs_fallback,
    }
