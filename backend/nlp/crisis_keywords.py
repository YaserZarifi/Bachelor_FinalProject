# آستانه‌ای که بالای آن گزارش «بحرانی» تلقی می‌شود
CRISIS_SCORE_THRESHOLD = 3


def calculate_crisis_score(text: str) -> tuple[int, list[str]]:
    """
    متن گزارش را بررسی کرده و امتیاز بحرانی بودن را برمی‌گرداند.

    Args:
        text: متن گزارش شهروند

    Returns:
        (score, matched_keywords): امتیاز کل و لیست کلمات تطابق‌یافته
    """
    text_lower = text.strip()
    score = 0
    matched = []

    for keyword, weight in CRISIS_KEYWORDS.items():
        if keyword in text_lower:
            score += weight
            matched.append(keyword)

    return score, matched


def is_crisis(text: str) -> tuple[bool, int, list[str]]:
    """
    تشخیص می‌دهد آیا گزارش بحرانی است یا خیر.

    Returns:
        (is_urgent, score, matched_keywords)
    """
    score, matched = calculate_crisis_score(text)
    return score >= CRISIS_SCORE_THRESHOLD, score, matched
